bool isExitExists(std::vector<std::vector<int>> map, int x, int y) {
    int FENCE = 1;
    int moves[4][2] = { {1, 0}, {-1, 0}, {0, 1}, {0, -1} };
    if (x >= map.size() || x < 0) {
        return true;
    }
    if (y >= map[0].size() || y < 0) {
        return true;
    }
    if (map[x][y] == FENCE) {
        return false;
    }
    map[x][y] = FENCE;
    for (auto move : moves) {
        bool res = isExitExists(map, x + move[0], y + move[1]);
        if (res) {
            return true;
        }
    }
    return false;
}


